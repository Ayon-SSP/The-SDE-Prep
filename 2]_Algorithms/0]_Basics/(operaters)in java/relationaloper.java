public class relationaloper {
    public static void main(String args[])
    {
        int a=1;
        int b=1;
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
    }
    
}
